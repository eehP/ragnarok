static struct option longopts[] = {
	{ "network",	no_argument,	NULL, 'n'},
	{ "xml",	required_argument, NULL, 'x'},
	{ "version", 	no_argument, 	NULL, 'v'},
	{ "help", 		no_argument, 	NULL, 'h'},
	{ NULL, 0, NULL, 0 }
};

void usage(int argc, char const *argv[]){
	char *name = NULL;
	name = strrchr(argv[0], '/');
	fprintf(stdout, "Usage : %s [OPTIONS]\n", (name ? name + 1: argv[0]));
	fprintf(stdout, "-x, --xml <xmlfile> \t\tXML file to parse\n");
	fprintf(stdout, "-h, --help\t\t\tprint this help\n");
	debug("DEBUG : ON\n");
}

int main(int argc, char const *argv[]){

	int opt, optindex = 0;
	int xconfig = 0;
	const char *xmlfile;
	while((opt = getopt_long(argc, (char* const *)argv, "vhx", longopts, &optindex)) != -1){
		switch(opt){
			case 'h':
				usage(argc, argv);
				return 0;
			case 'v' :
				version();
				return 0;
			case 'x' :
				xmlfile = argv[optind];
				debug("%s\n", xmlfile);
				xconfig = 1;
				break;
		}
	}
	debug("%s\n", logo);
	if (!xconfig){
		#ifdef RELEASE
		xmlfile = "/etc/ragnarok/client.xml";
		#else
		xmlfile = "config/client.xml";
		#endif
	}

	parse_config_file(xmlfile);
	debug("ip : %s\n", ipaddr);
	debug("port : %s\n", port);
	debug("iface : %s\n", iface);

	/* Check for IP adddr and port */
	if (init_client(0, ipaddr, port, &results) < 0){
		exit(EXIT_FAILURE);
	}
	if ((sock = socket(AF_INET, SOCK_STREAM, 0)) < 0) {
		perror("socket");
		exit(EXIT_FAILURE);
	}
	if (connect(sock, results->ai_addr, results->ai_addrlen) < 0) {
		perror("connect");
		exit(EXIT_FAILURE);
	}
	freeaddrinfo(results);

	// I grab iface value in config.xml.
	// Idea is to use config.xml instead of hardcoded values in code
	get_mac(iface);
	if (argc == 2)
	{
		run_iwlist(argv[1]); // wlo1 for Ubuntu, not sure about other distros
	} else {
		run_iwlist("wlan0");
	}
	close(sock);
	return 0;
}
